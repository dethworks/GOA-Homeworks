class Person:
    def __init__(self, name, surname, age):
        self.name = name
        self.surname = surname
        self.age = age

    def person_info(self):
        print(f"name: {self.name}")
        print(f"surname: {self.surname}")
        print(f"age: {self.age}")

person = Person("Luka", "Talaxaze", 15)
